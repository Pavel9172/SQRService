public class SQRService {
    public static void main(String[] args) {

        int j = 0;


        for (int i = 10; i <= 99; i++){
            if (i * i >= 200 && i * i <= 300){

                System.out.println( "значения" +i);
                j++;










            }
        }
        System.out.println("конец"  +j  );
    }

}